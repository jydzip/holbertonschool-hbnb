class Users:
    def __init__(self, id: int, username: str, password: str, email: str, first_name: str, last_name: str, age: int):
        self.id = id
        self.username = username
        self.password = password
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.age = age
        
